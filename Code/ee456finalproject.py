# Custom Dataset Class
class ImageDataset(Dataset):
    def __init__(self, X, y):
        self.X = X
        self.y = y

    def __len__(self):
        return len(self.X)

    def __getitem__(self, idx):
        try:
            img = self.X[idx]
            label = self.y[idx]
        except IndexError:
            raise IndexError(f"Index {idx} is out of bounds for the dataset of size {len(self.X)}")
        except KeyError:
            raise KeyError(f"KeyError at index {idx}. Dataset X has shape {self.X.shape}, y has length {len(self.y)}")

        img = torch.tensor(img, dtype=torch.float32)
        label = torch.tensor(label, dtype=torch.long)
        return img, label

# CNN Model Architecture
class CNNModel(nn.Module):
    def __init__(self, num_classes):
        super(CNNModel, self).__init__()

        self.conv1 = nn.Conv2d(1, 32, kernel_size=3, padding=1)
        self.conv2 = nn.Conv2d(32, 64, kernel_size=3, padding=1)
        self.conv3 = nn.Conv2d(64, 128, kernel_size=3, padding=1)
        self.pool = nn.MaxPool2d(2, 2)

        self.fc1 = nn.Linear(128 * 16 * 16, 512)
        self.fc2 = nn.Linear(512, num_classes)

    def forward(self, x):
        x = self.pool(F.relu(self.conv1(x)))
        x = self.pool(F.relu(self.conv2(x)))
        x = self.pool(F.relu(self.conv3(x)))

        x = x.view(-1, 128 * 16 * 16)  # Flatten the output
        x = F.relu(self.fc1(x))
        x = self.fc2(x)

        return x

