def combine_features(pred_labels, conf_scores, category_encoded):
    """Combine predicted labels, confidence scores, and category encoded into a single feature vector."""
    combined_features = []

    for label_list, score_list, cat in zip(pred_labels, conf_scores, category_encoded):
        # Flatten the label and score lists, and add the category
        feature_vector = list(label_list) + list(score_list) + [cat]
        combined_features.append(feature_vector)

    # Convert the list of feature vectors into a numpy array with shape (num_samples, num_features)
    return np.array(combined_features)

